//Numbers with operators
let x = 6
let y = 15

let addition = x + y;
let subtraction = x - y;
let multiplication = x * y;
let division = x / y;
let modulus = x % y;
let increment = x++;
let decrement = y--;

//Comparison operators 

let a = 3;
let b = 8;

let isEqual = a == b; // false (equal)
let isNotEqual = a!= b; // true (not equal)
let isGreater = a > b; // false
let isLess = a < b; // true
let isGreaterOrEqual = a >= b; //false
let isLessOrEqual = a <= b; // true

//Strings with operators 
let firstName = "John";
let lastName = "Doe";
let fullName = firstName + " " + lastName; // "John Doe"

//Booleans (true/false)
let isTrue = true; 
let isFalse = false;

let andResult = isTrue && isFalse; // false since they have different boolean value
let orResult = isTrue || isFalse; // true since values differ
let notResult = !isTrue; // false, using the opposite of value "true" assigned

